#include<stdio.h>
#include<conio.h>

void main()
{
    int x=5;
    x++;    //post increment operator
    printf("x=%d\n",x);
    ++x;    //pre increment operator
    printf("x=%d\n",x);
    x--;    //post decrement operator
    printf("x=%d\n",x);
    --x;    //pre decrement operator
    printf("x=%d\n",x);
    getch();
}

//Program for increment and decrement operator